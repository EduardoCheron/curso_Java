public class processamento_atribuicao {

	public static void main(String[] args) {
	
		double b, B, h, area;
		
		b = 6.0;
		B = 8.0;
		h = 5.0;
		
		area = (b + B) / 2.0 * h; 
		
		System.out.println(area);
			
		int a, x;
		double resultado;
		
		a = 5;
		x = 2;
		
		resultado = (double) a / x;
		
		System.out.println(resultado);
		
	}
	
}
